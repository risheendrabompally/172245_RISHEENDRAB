package com.spring;

public class Student {
	private String name;  
	  
	
	public Student() {
		super();
	}

	public String getName() {  
	    return name;  
	}  
	  
	public void setName(String name) {  
	    this.name = name;  
	}  
	  
	public void show(){  
	    System.out.println(name);  
	}  
}
